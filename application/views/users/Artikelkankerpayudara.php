                <h3 class="card-title text-left">Kanker Payudara</h3>
                <p class="card-text text-left">
                    Kanker payudara merupakan suatu jenis tumor ganas yang berkembang pada sel-sel payudara. Kanker ini dapat tumbuh jika terjadi pertumbuhan yang abnormal 
                    dari sel-sel pada payudara. Sel-sel tersebut membelah diri lebih cepat dari sel normal dan berakumulasi, yang kemudian membentuk benjolan atau massa. 
                    Pada stadium yang lebih parah, sel-sel abnormal ini dapat menyebar melalui kelenjar getah bening ke organ tubuh lainnya.
                </p>
                <h3>Gejala Kanker Payudara</h3>
                <p>
                    Pada stadium dini, kanker payudara dapat tidak menunjukkan gejala tertentu. Oleh karena itu, sangat penting untuk melakukan SADARI atau periksa payudara sendiri setiap bulan, 
                    10 hari setelah masa haid berakhir. Raba dengan teliti searah jarum jam payudara untuk mendeteksi adanya benjolan atau perubahan pada payudara. Beberapa gejala kanker payudara, antara lain:
                </p>
                 <ul>
                    <li>Benjolan atau pengerasan pada payudara yang berbeda dari jaringan sekitar.</li>
                    <li>Darah keluar dari puting payudara.</li>
                    <li>Kemerahan atau pembesaran pori-pori kulit payudara, yang menyerupai kulit jeruk.</li>
                    <li>Nyeri dan pembengkakan pada payudara.</li>
                    <li>Pengelupasan kulit di sekitar puting payudara.</li>
                    <li>Perubahan pada kulit payudara, seperti cekungan.</li>
                    <li>Perubahan ukuran, bentuk, atau tampilan dari payudara.</li>
                    <li>Puting tertarik masuk (retraksi atau inversi) ke dalam.</li>
                    <li>Benjolan atau pembengkakan di bawah ketiak.</li>
                </ul>
                 <h4>Penyebab dan Faktor Risiko Kanker Payudara</h4>
                 <p>Kanker payudara terjadi akibat pertumbuhan abnormal dari sel-sel pada payudara. Pertumbuhan abnormal ini diduga disebabkan oleh mutasi gen yang diturunkan secara genetik. 
                    Selain itu, terdapat beberapa faktor risiko yang diduga menjadi pemicu kanker payudara, yaitu:</p>
                 <ul>
                    <li>Jenis kelamin wanita jauh lebih tinggi dibandingkan pria.</li>
                    <li>Usia yang bertambah, paling banyak pada usia di atas 50 tahun.</li>
                    <li>Kebiasaan merokok atau minum minuman beralkohol.</li>
                    <li>Kelebihan berat badan atau obesitas.</li>
                    <li>Mulai menopause pada usia lebih tua, yaitu setelah usia 55 tahun.</li>
                    <li>Mulai menstruasi sebelum usia 12 tahun.</li>
                    <li>Penggunaan alat kontrasepsi hormon dan terapi hormon setelah menopause.</li>
                    <li>Riwayat kanker payudara pada diri sendiri pada salah satu payudara.</li>
                    <li>Riwayat kanker payudara pada nenek, ibu, tante, adik, kakak, atau anak sekandung.</li>
                    <li>Riwayat terpapar dengan radiasi.</li>
                    <li></li>
                 </ul>
                 <h4>Diagnosis Kanker Payudara</h4>
                 <p>Dokter akan mendiagnosis kanker payudara dengan melakukan wawancara medis lengkap, pemeriksaan fisik langsung untuk 
                    mendeteksi perubahan pada payudara serta kelenjar getah bening pada ketiak, serta pemeriksaan penunjang, seperti:
                 </p>
                 <ul>
                     <li>Mammogram atau foto payudara, untuk mendeteksi kelainan pada payudara.</li>
                     <li>Ultrasonografi (USG) payudara, untuk menentukan benjolan payudara berupa massa padat atau kista yang berisi cairan.</li>
                     <li>Biopsi dengan pengambilan sampel jaringan, untuk diperiksa di laboratorium dan menentukan sel yang diperiksa bersifat jinak atau ganas.</li>
                     <li>Computerized Tomography scan (CT scan) dan Magnetic Resonance Imaging (MRI), untuk menentukan ukuran serta penyebaran dari kanker payudara.</li>
                 </ul>
                 <h4>Pencegahan Kanker Payudara</h4>
                 <p>Beberapa faktor risiko kanker hati, antara lain:</p>
                 <ul>
                   <li>Berolahraga secara rutin.</li>
                   <li>Cukup istirahat.</li>
                   <li>Menyusui anak hingga berusia dua tahun.</li>
                   <li>Pemeriksaan rutin dan teliti dengan SADARI.</li>
                   <li>Pengelolaan stres yang baik.</li>
                   <li>Pola makan gizi seimbang.</li>
                   <li>Tidak merokok atau minum minuman beralkohol.</li>
                 </ul>
                 <h4>Pengobatan Kanker Payudara</h4>
                 <p>Penanganan kanker payudara bergantung pada jenis kanker yang dialami, stadium kanker, ukuran massa, serta sensitivitas sel kanker terhadap hormon. 
                    Dokter akan menentukan terapi yang paling sesuai dengan keadaan penderita berdasarkan hal-hal tersebut. Beberapa pilihan pengobatan pada kanker payudara, 
                    antara lain:
                </p>
                 <ul>
                   <li>Pembedahan, yang meliputi pengangkatan kanker atau benjolan (lumpektomi), pengangkatan seluruh payudara (mastektomi), pengangkatan jumlah terbatas dari 
                       kelenjar limfe (sentinel node biopsy), atau pengangkatan beberapa kelenjar limfe (axillary lymph node dissection).</li>
                   <li>Radioterapi, yang dilakukan dengan menggunakan energi sinar X dan proton, untuk mematikan sel-sel kanker.</li>
                   <li>Kemoterapi, yang dilakukan dengan menggunakan obat-obatan tertentu, untuk mematikan sel kanker.</li>
                   <li>Terapi penghambat hormon, jika kanker diketahui sensitif terhadap hormon estrogen atau progesteron.</li>
                 </ul>
                 <p>Umumnya, pengidap akan menjalani prosedur pembedahan untuk kanker payudara dan mendapatkan penanganan lain sebelum dan/atau sesudah pembedahan, seperti kemoterapi, terapi hormonal, atau terapi radiasi.</p>
