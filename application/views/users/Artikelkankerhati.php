                <h3 class="card-title text-left">Kanker Hati</h3>
                <p class="card-text text-left">Hati merupakan organ yang berfungsi untuk menetralisir racun di dalam tubuh, membentuk protein dan faktor pembekuan darah,
                                               serta memetabolisme beberapa hormon. Kanker hati adalah suatu tumor ganas yang bermula dari organ hati. Kondisi ini terjadi ketika 
                                               sel-sel di dalam hati bermutasi dan membentuk tumor. Terdapat dua jenis kanker hati, yaitu:
                 </p>
                 <ul>
                   <li>Kanker yang berasal dari sel hati (hepatoma).</li>
                   <li>Kanker yang berasal dari organ lain yang menjalar ke hati (metastasis).</li>
                 </ul>
                 <h4>Gejala Kanker Hati</h4>
                 <p>Gejala umum dari kanker hati,antara lain</p>
                 <ul>
                    <li>Berat badan menurun tanpa penyebab yang jelas.</li>
                    <li>Demam berkepanjangan yang tidak jelas penyebabnya.</li>
                    <li>Gangguan kesadaran</li>
                    <li>Gatal di seluruh tubuh.</li>
                    <li>Kehilangan nafsu makan.</li>
                    <li>Mata dan kulit terlihat kuning (jaundice).</li>
                    <li>Mual dan muntah.</li>
                    <li>Pembesaran limpa.</li>
                    <li>Penumpukan cairan dalam perut (asites).</li>
                    <li>Perut terasa penuh meski hanya makan sedikit.</li>
                    <li>Rasa tidak nyaman atau nyeri pada perut kanan atas.</li>
                    <li>Tinja berwarna putih seperti kapur.</li>
                    <li>Tubuh mudah lelah dan lemas.</li>
                    <li>Tubuh mudah memar dan mengalami perdarahan.</li>
                    <li>Ukuran hati membesar.</li>
                    <li>Urine berwarna gelap.</li>
                 </ul>
                 <h4>Penyebab Kanker Hati</h4>
                 <p>Penyebab kanker hati adalah mutasi atau perubahan sel-sel pada organ hati, 
                   yang berakibat tidak terkendalinya pertumbuhan sel, sehingga membentuk tumor. 
                   Kendati demikian, penyebab dari mutasi sel tersebut belum diketahui secara pasti.
                 </p>
                 <h4>Faktor Risiko</h4>
                 <p>Beberapa faktor risiko kanker hati, antara lain:</p>
                 <ul>
                   <li>Infeksi kronis hepatitis B atau hepatitis C.</li>
                   <li>Kondisi tertentu, seperti diabetes, lupus, atau obesitas.</li>
                   <li>Konsumsi alkohol berlebihan.</li>
                   <li>Menjalani operasi pengangkatan kandung empedu (kolesistektomi).</li>
                   <li>Merokok.</li>
                   <li>Paparan aflatoksin, suatu racun dari jamur yang mengontaminasi tumbuh-tumbuhan yang akan dijadikan makanan, serta tidak disimpan dengan baik.</li>
                   <li>Paparan radiasi dari foto Rontgen atau CT scan.</li>
                   <li>Paparan zat kimia, seperti arsenik, vinil klorida, dan trichloroethylene.</li>
                   <li>Penyakit hati turunan, seperti hemokromatosis dan penyakit Wilson.</li>
                   <li>Penyakit perlemakan hati.</li>
                   <li>Sirosis atau terbentuknya jaringan parut di hati.</li>
                   <li>Sistem kekebalan tubuh lemah, misalnya karena menderita HIV/AIDS atau baru menjalani transplantasi organ tubuh.</li>
                 </ul>
                 <h4>Diagnosis Kanker Hati</h4>
                 <p>Dokter akan mendiagnosis kanker hati dengan melakukan wawancara medis, pemeriksaan fisik, serta pemeriksaan penunjang seperti:</p>
                 <ul>
                   <li>Pemeriksaan darah, terutama untuk menilai fungsi organ hati serta kadar Alpha Fetoprotein (AFP) dalam darah.</li>
                   <li>Pencitraan dengan USG, CT scan, atau MRI, untuk mengetahui bentuk hati, keberadaan tumor, lokasi kanker, serta penyebarannya</li>
                   <li>Biopsi hati, dengan mengambil sedikit jaringan hati untuk diperiksa di laboratorium.</li>
                   <li>Laparoskopi, untuk memeriksa kondisi bagian dalam perut dengan menggunakan selang berkamera.</li>
                 </ul>
                 <h4>Pencegahan Kanker Hati</h4>
                 <p>Beberapa upaya yang dapat dilakukan untuk mencegah kanker hati, antara lain:</p>
                 <ul>
                   <li>Melakukan hubungan intim yang aman.</li>
                   <li>Mendapatkan vaksin hepatitis B.</li>
                   <li>Menggunakan alat pelindung diri saat terpapar bahan kimia.</li>
                   <li>Mengurangi konsumsi alkohol.</li>
                   <li>Menjaga berat badan tetap ideal.</li>
                   <li>Menjauhi NAPZA, terutama dalam bentuk suntik.</li>
                 </ul>
                 <h4>Pengobatan Kanker Hati</h4>
                 <p>Jika tumor masih berukuran kecil, pengobatan utama adalah dengan melakukan operasi pengangkatan tumor, yang dapat dilanjutkan dengan transplantasi hati.
                    Namun, jika kondisi tumor sudah berukuran besar dan tidak dapat dioperasi, pengobatan yang dapat dilakukan adalah dengan kemoterapi dan radiasi untuk mengecilkan 
                    ukuran tumor dan meringankan gejala yang dialami penderitanya, tetapi tidak dapat menghilangkan tumor sepenuhnya.
                  </p>
