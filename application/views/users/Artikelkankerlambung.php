                 <h4>Penyebab dan Faktor Risiko Kanker Lambung</h4>
                 <p>
                    Kanker lambung terjadi ketika sel-sel pada bagian tertentu di lambung berkembang secara tidak terkendali. Penyebab dari pertumbuhan tidak terkendali ini belum diketahui secara pasti. Namun demikian, 
                    terdapat beberapa faktor risiko yang diduga dapat memicu kanker lambung, antara lain:
                 </p>
                 <ul>
                     <li>Berusia di atas 55 tahun.</li>
                     <li>Jenis kelamin laki-laki.</li>
                     <li>Golongan darah A.</li>
                     <li>Infeksi bakteri H. Pylori.</li>
                     <li>Kebiasaan merokok.</li>
                     <li>Pengidap tukak lambung, anemia pernisiosa, atau polip lambung.</li>
                     <li>Pola makan tinggi garam, acar, processed food, daging merah, namun rendah serat.</li>
                     <li>Riwayat keluarga yang mengidap kanker lambung.</li>
                     <li>Riwayat pernah menjalani operasi pada lambung.</li>
                     <li>Pengidap kanker limfoma, kanker sel darah putih, kanker esofagus, kanker usus, kanker prostat, kanker serviks, dan kanker paru-paru.</li>
                 </ul>
                 <h4>Diagnosis Kanker Lambung</h4>
                 <p>
                    Dokter akan mendiagnosis kanker lambung dengan melakukan wawancara medis, pemeriksaan fisik, serta pemeriksaan penunjang, antara lain:
                 </p>
                 <ol type="1">
                     <li>Pemeriksaan darah, untuk mendeteksi anemia akibat pendarahan.</li>
                     <li>Endoskopi, untuk memeriksa bagian dalam lambung.</li>
                     <li>Biopsi, dengan pemeriksaan sampel jaringan yang mencurigakan di laboratorium.</li>
                     <li>Ultrasound endoskopi, barium meal X-ray, CT scan, PET scan, dan laparoskopi, untuk mendeteksi kanker lambung serta penyebarannya ke organ lain.</li>
