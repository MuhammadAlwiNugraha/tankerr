                    Kanker lambung adalah suatu jenis tumor ganas yang tumbuh pada lambung, yaitu organ pencernaan berbentuk kantong di tengah rongga perut manusia. 
                    Terdapat beberapa jenis kanker lambung yang paling umum, antara lain:
                </p>
                <ul>
                    <li>Adenocarcinoma, yaitu kanker lambung yang menyerang sel-sel mukosa, yaitu pelapis bagian dalam lambung.</li>
                    <li>Carcinoid tumor, yaitu kanker lambung yang menyerang sel-sel penghasil hormon pada lambung.</li>
                    <li>Gastrointestinal Stromal Tumor (GIST), yaitu kanker lambung yang menyerang jaringan ikat atau otot-otot dinding perut.</li>
                    <li>Limfoma lambung, yaitu kanker lambung yang menyerang sel-sel imun yang dapat ditemukan pada dinding lambung.</li>
                    <li>Squamous cell carcinoma, small cell carcinoma, dan leiomyosarcoma, yaitu jenis-jenis kanker lambung yang lebih jarang ditemukan.</li>
                </ul>
