              <div style="text-align: left;" >
                <h3 class="card-title text-left">Kanker Paru-paru</h3>
                <p class="card-text text-left">
                    Kanker paru-paru adalah suatu kondisi di mana sel-sel tumbuh secara tidak terkendali di dalam paru-paru (organ yang berfungsi untuk menyebarkan 
                    oksigen ke dalam darah saat menghirup napas dan membuang karbondioksida saat menghela napas).
                </p>
                <h3>Gejala Kanker Paru</h3>
                <p>
                    Sebagian besar gejala kanker paru terjadi di paru-paru, tapi juga mungkin mengalami gejala lain pada tubuh. Hal ini karena kanker telah menyebar 
                    (dalam istilah medis disebut metastasis) ke bagian tubuh lainnya. Tingkat keparahan gejala juga berbeda. Beberapa bahkan mungkin tidak merasakan gejala atau 
                    hanya merasa lelah pada umumnya. Beberapa gejala yang harus diketahui, meliputi:
                </p>
                 <ul>
                    <li>Ketidaknyamanan atau nyeri pada dada</li>
                    <li>Batuk yang tidak hilang atau semakin memburuk dari waktu ke waktu</li>
                    <li>Masalah pernapasan</li>
                    <li>Mengi</li>
                    <li>Darah dalam dahak (lendir batuk dari paru-paru)</li>
                    <li>Suara serak</li>
                    <li>Masalah dalam menelan</li>
                    <li>Kehilangan selera makan</li>
                    <li>Kehilangan berat badan tanpa alasan yang diketahui</li>
                    <li>Merasa sangat lelah</li>
                    <li>Peradangan atau sumbatan di paru-paru</li>
                    <li>Pembengkakan atau pembesaran kelenjar getah bening dalam dada di daerah paru-paru.</li>
                </ul>
                 <h4>Penyebab Kanker Paru</h4>
                 <p>
                    Kebanyakan kasus dari kanker paru-paru disebabkan karena rokok. Rokok adalah faktor risiko terbesar dari kanker paru. 
                    Rokok mengandung 60 zat racun yang berbeda, sehingga dapat menyebabkan kanker. Zat racun tersebut dikenal sebagai zat karsinogenik.
                    <br><br>
                    Jika seseorang merokok 25 batang sehari, berarti orang tersebut berisiko 25 kali lebih besar untuk mengidap kanker paru daripada orang yang tidak merokok. 
                    Mengonsumsi tembakau lain dengan cara berbeda, seperti cerutu atau kunyah, juga dapat menyebabkan kanker paru dan kanker lainnya. Misalnya, kanker esofagus dan mulut.
                    <br><br>
                    Menghisap ganja juga meningkatkan risiko terkena kanker paru. Perokok pasif juga meningkatkan risiko kanker paru. Riset membuktikan bahwa perokok pasif wanita yang 
                    pasangannya merokok, maka memiliki risiko 25 persen lebih besar daripada yang tinggal dengan orang yang tidak merokok.
                    <br><br>
                    Paparan terhadap zat racun lainya seperti arsen, asbes, silika, bau gas atau bensin, gas NO (hasil dari kendaraan), dan sebagainya juga meningkatkan risiko terkena kanker paru.
                </p>
                 <h4>Faktor Risiko Kanker Paru</h4>
                 <p>
                    Merokok bisa dikatakan sebagai penyebab utama kanker paru-paru. Orang yang paling berisiko terkena kanker paru-paru adalah perokok aktif. 
                    Sekitar 85 persen kanker paru-paru dikaitkan dengan kebiasaan merokok. Meski begitu, bukan berarti setiap perokok akan terkena kanker paru-paru. 
                    Selain itu, orang yang tidak merokok juga berkemungkinan terserang kanker paru-paru, meski lebih rendah jumlahnya.
                    <br><br>
                    Selain rokok, beberapa penyebab kanker paru-paru lain adalah menghirup arsenik, radiasi, dan polusi udara. Kanker paru-paru juga lebih umum terjadi pada orang yang sudah lanjut usia.
                 </p>
                 <h4>Diagnosis Kanker Paru</h4>
                 <p>
                    Dokter akan menentukan diagnosis kanker paru-paru dari gejala dan pemeriksaan fisik. Pemeriksaan penunjang lainnya juga diperlukan. Pemeriksaan spirometri untuk memeriksa fungsi paru 
                    dengan mengukur jumlah udara yang keluar dan masuk ketika bernapas. Pemeriksaan darah biasa dilakukan untuk menyingkirkan kemungkinan infeksi.
                    <br><br>
                    Pemeriksaan radiologi juga dibutuhkan dalam mendiagnosis kanker paru. Pemeriksaan sinar X dada biasanya dilakukan pertama kali dan dapat menunjukkan adanya massa berwarna putih keabuan. 
                    Namun, penampakan tersebut juga dapat disebabkan oleh abses paru.
                    <br><br>
                    Pemeriksaan radiologi lain, seperti CT scan, PET-CT scan dapat memeriksa kanker paru secara lebih mendetail. Jika sudah dipastikan memiliki kanker paru, prosedur bronkoskopi dan biopsi akan dilakukan. 
                    Sampel jaringan yang diambil dari biopsi akan diperiksa.
                 </p>
                 <h4>Pencegahan Kanker Paru</h4>
                 <p>
                    Cara terbaik untuk mencegah kanker paru-paru adalah dengan menghindari rokok dan asapnya. Berhenti merokok selama 10 tahun menurunkan risiko kanker paru hingga 50 persen.
                    <br><br>
                    Riset juga membuktikan bahwa diet rendah lemak, tinggi serat, dan banyak mengonsumsi sayur, buah serta biji-bijian dapat menurunkan risiko kanker paru dan kanker lainnya. 
                    Selain itu, olahraga secara teratur juga ditemukan dapat membantu menurunkan risiko kanker paru dan kanker lainnya. Orang dewasa minimal harus melakukan olahraga aerobik intensitas sedang 150 menit seminggu.
                 </p>
                 <h4>Pengobatan Kanker Paru</h4>
                 <p>
                    Pengobatan kanker paru-paru tergantung dari tipe kankernya dan kondisi tubuh secara umum. Jika kanker didiagnosis pada tahap awal dan hanya di area kecil, akan membuat hasil perbedaan yang lebih baik pada hasil pengobatannya.
                    <br><br>
                    Pembedahan mengangkat kanker paru dapat dilakukan. Jika kondisi kesehatan pengidap melemah, radioterapi dapat dilakukan untuk menghancurkan sel kanker. Jika kanker sudah menyebar, pembedahan dan radioterapi tidak dapat dilakukan. 
                    Kemoterapi biasanya digunakan untuk kasus tersebut.
                 </p>
